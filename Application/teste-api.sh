#!/bin/bash
# ?? Script de Teste da API

echo "???????????????????????????????????????????????????????????????"
echo "?? TESTE DA API DE AUTENTICAÇĂO"
echo "???????????????????????????????????????????????????????????????"
echo ""

API="https://localhost:7126/api/auth"

# Cores para output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${YELLOW}1?? TESTANDO REGISTRO${NC}"
echo "Endpoint: POST /api/auth/register"
echo ""

REGISTER_RESPONSE=$(curl -s -X POST "$API/register" \
  -H "Content-Type: application/json" \
  -d '{
    "email":"test@example.com",
    "password":"senha123456",
    "name":"Test User"
  }' -k)

echo "Response: $REGISTER_RESPONSE"
TOKEN=$(echo $REGISTER_RESPONSE | grep -o '"token":"[^"]*"' | cut -d'"' -f4)

if [ -z "$TOKEN" ]; then
  echo -e "${RED}? Falha ao registrar${NC}"
else
  echo -e "${GREEN}? Registro bem-sucedido!${NC}"
  echo "Token: $TOKEN"
  echo ""
fi

echo ""
echo -e "${YELLOW}2?? TESTANDO LOGIN${NC}"
echo "Endpoint: POST /api/auth/login"
echo ""

LOGIN_RESPONSE=$(curl -s -X POST "$API/login" \
  -H "Content-Type: application/json" \
  -d '{
    "email":"test@example.com",
    "password":"senha123456"
  }' -k)

echo "Response: $LOGIN_RESPONSE"
TOKEN=$(echo $LOGIN_RESPONSE | grep -o '"token":"[^"]*"' | cut -d'"' -f4)

if [ -z "$TOKEN" ]; then
  echo -e "${RED}? Falha ao fazer login${NC}"
else
  echo -e "${GREEN}? Login bem-sucedido!${NC}"
  echo "Token: $TOKEN"
  echo ""
fi

echo ""
echo -e "${YELLOW}3?? TESTANDO PERFIL${NC}"
echo "Endpoint: GET /api/auth/profile [Authorize]"
echo ""

if [ -z "$TOKEN" ]; then
  echo -e "${RED}? Sem token, pulando teste de perfil${NC}"
else
  PROFILE_RESPONSE=$(curl -s -X GET "$API/profile" \
    -H "Authorization: Bearer $TOKEN" \
    -k)
  
  echo "Response: $PROFILE_RESPONSE"
  
  if echo $PROFILE_RESPONSE | grep -q "test@example.com"; then
    echo -e "${GREEN}? Perfil obtido com sucesso!${NC}"
  else
    echo -e "${RED}? Falha ao obter perfil${NC}"
  fi
fi

echo ""
echo "???????????????????????????????????????????????????????????????"
echo -e "${GREEN}? Testes concluídos!${NC}"
echo "???????????????????????????????????????????????????????????????"
